/* Q3: Apply WHERE & Operators*/


--- Display all students whose age is greater than 20.----

SELECT * FROM STUDENTS WHERE AGE>20

----Display all courses of the "Computer Science" department.---

SELECT * FROM COURSES WHERE DEPARTMENT_ID=1
------------------ OR -----------------------
SELECT COU.COURSE_NAME FROM COURSES AS COU
JOIN DEPARTMENTS AS DEP
ON COU.DEPARTMENT_ID=DEP.DEPARTMENTID 
WHERE DEP.DEPARTMENT_NAME='COMPUTER_SCIENCE'


----Display all students who belong to the "Electronics" department.----

SELECT * FROM STUDENTS WHERE DEPARTMENT_ID=4
------------------ OR -----------------------
SELECT STU.NAME, DEP.DEPARTMENT_NAME FROM STUDENTS AS STU
JOIN DEPARTMENTS AS DEP 
ON STU.DEPARTMENT_ID=DEP.DEPARTMENTID
WHERE DEP.DEPARTMENT_NAME='ELECTRONICS'

-----Display students whose age is between 18 and 22.-----

SELECT * FROM STUDENTS WHERE AGE BETWEEN 18 AND 22


/* Q4: Joins */

---- INNER JOIN Students with Departments (to show student names with their department names).----

SELECT STU.*, DEP.DEPARTMENT_NAME FROM STUDENTS AS STU
INNER JOIN DEPARTMENTS AS DEP 
ON STU.DEPARTMENT_ID=DEP.DEPARTMENTID


---- LEFT JOIN Courses with Departments (to show all courses, even if some department does not exist).----

SELECT COU.COURSE_NAME, DEP.DEPARTMENT_NAME FROM COURSES AS COU
LEFT JOIN DEPARTMENTS AS DEP 
ON COU.DEPARTMENT_ID=DEP.DEPARTMENTID
--ORDER BY COU.DEPARTMENT_ID

----- RIGHT JOIN Students with Courses (to show all courses, even if no student has enrolled in them).----

SELECT STU.NAME, COU.COURSE_NAME FROM STUDENTS AS STU
RIGHT JOIN COURSES AS COU
ON STU.DEPARTMENT_ID=COU.DEPARTMENT_ID